// lastIndexOf method is used to find the last occurrence of a specified element in an array. 
//It returns the index of the last occurrence of the element, or -1 if the element is not found. 
//The search is performed backwards, starting from the end of the array and moving towards the beginning.
//syntax - lastIndexOf(searchElement, fromIndex)

let x=[1,2,3,4,2,5]
console.log(x.lastIndexOf(2));  //it check from backside and give its index value
