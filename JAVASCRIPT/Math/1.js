const num1 = 4.7;
const num2 = 4.3;
const num3 = -4.3;

console.log(Math.floor(num1)); // Output: 4
console.log(Math.floor(num2)); // Output: 4
console.log(Math.floor(num3)); // Output: -5

console.log(Math.ceil(num1));  // Output: 5
console.log(Math.ceil(num2));  // Output: 5
console.log(Math.ceil(num3));  // Output: -4

console.log(Math.round(num1)); // Output: 5
console.log(Math.round(num2)); // Output: 4
console.log(Math.round(-4.5)); // Output: -4
console.log(Math.round(-4.6)); // Output: -5
